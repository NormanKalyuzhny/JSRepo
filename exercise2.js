//switch-case
let a = 4;
let b = 2;

switch(0) { // 0 is for default
    case 1:  
      console.log(a + b);
      break;

    case 2:  
      console.log(a - b);
      break;

    case 3:  
      console.log(a * b);
      break;

    case 4:  
      console.log(a / b);
      break;          
    
    default:
    console.log('nope');
    break;
  }